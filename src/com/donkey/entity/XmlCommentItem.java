package com.donkey.entity;

public class XmlCommentItem {
	private String memoryId;
	private String memoryCommentId;
	private String nickname;
	private String mcContent;
	private String mcPubdate;

	public String getMemoryId() {
		return memoryId;
	}

	public void setMemoryId(String memoryId) {
		this.memoryId = memoryId;
	}

	public String getMemoryCommentId() {
		return memoryCommentId;
	}

	public void setMemoryCommentId(String memoryCommentId) {
		this.memoryCommentId = memoryCommentId;
	}

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public String getMcContent() {
		return mcContent;
	}

	public void setMcContent(String mcContent) {
		this.mcContent = mcContent;
	}

	public String getMcPubdate() {
		return mcPubdate;
	}

	public void setMcPubdate(String mcPubdate) {
		this.mcPubdate = mcPubdate;
	}

}
