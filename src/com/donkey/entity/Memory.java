package com.donkey.entity;


public class Memory{
	private int memoryId;
	private String memoryTitle;
	private String memoryLastModifyTime;
	private String memoryContent;
	private String memoryLocation;
	private int memoryUserId;
	public int getMemoryId() {
		return memoryId;
	}
	public void setMemoryId(int memoryId) {
		this.memoryId = memoryId;
	}
	public String getMemoryTitle() {
		return memoryTitle;
	}
	public void setMemoryTitle(String memoryTitle) {
		this.memoryTitle = memoryTitle;
	}
	public String getMemoryLastModifyTime() {
		return memoryLastModifyTime;
	}
	public void setMemoryLastModifyTime(String memoryLastModifyTime) {
		this.memoryLastModifyTime = memoryLastModifyTime;
	}
	public String getMemoryContent() {
		return memoryContent;
	}
	public void setMemoryContent(String memoryContent) {
		this.memoryContent = memoryContent;
	}
	public String getMemoryLocation() {
		return memoryLocation;
	}
	public void setMemoryLocation(String memoryLocation) {
		this.memoryLocation = memoryLocation;
	}
	public int getMemoryUserId() {
		return memoryUserId;
	}
	public void setMemoryUserId(int memoryUserId) {
		this.memoryUserId = memoryUserId;
	}
}
